"    def forward(self, features, labels):\n"
"        # features: (2B, 128), labels: (B,)\n"
"        B = labels.shape[0]\n"
"        labels_rep = labels.repeat(2)  # (2B,)\n"
"        mask = (labels_rep.unsqueeze(0) == labels_rep.unsqueeze(1)).float()  # (2B,2B)\n"
"        # 자기 자신 제외\n"
"        eye = torch.eye(2*B, device=features.device)\n"
"        mask = mask - eye\n"
"        # 유사도\n"
"        sim = torch.matmul(features, features.T) / self.T  # (2B,2B)\n"
"        sim = sim - eye * 1e9  # self 제외\n"
"        # log-sum-exp\n"
"        log_prob = sim - torch.logsumexp(sim, dim=1, keepdim=True)\n"
"        # positive 평균\n"
"        n_pos = mask.sum(1).clamp(min=1)\n"
"        loss = -(mask * log_prob).sum(1) / n_pos\n"
"        return loss.mean()\n"
