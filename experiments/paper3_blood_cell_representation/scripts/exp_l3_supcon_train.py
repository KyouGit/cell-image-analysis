"# SupCon Loss (Khosla et al. 2020)\n"
"class SupConLoss(nn.Module):\n"
"    def __init__(self, temperature=0.07, hard_pairs=None, hard_weight=2.0):\n"
"        super().__init__()\n"
"        self.T = temperature\n"
"        self.hard_pairs = set()\n"
"        if hard_pairs:\n"
"            for a,b in hard_pairs:\n"
"                self.hard_pairs.add((a,b)); self.hard_pairs.add((b,a))\n"
